class Solution:
    def twoSum(self, nums: List[int], target: int) -> List[int]:
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """

        for n,i in enumerate(nums):
            for m,j in enumerate(nums):
                if n == m:
                    continue
                if target == i + j:
                    return (n, m)
                    break
